package com.ewu.career.entity;

/**
 * Enumeration representing the 16 distinct types of applied learning required by EWU RFP 26-VMD04.
 */
public enum LearningType {
    /** 1. Professional work experience for credit or pay */
    INTERNSHIP,

    /** 2. Supervised practical application of previously studied theory */
    PRACTICUM,

    /** 3. Hands-on health-related training in a clinical setting */
    CLINICAL,

    /** 4. Alternating periods of academic study and full-time employment */
    CO_OP,

    /** 5. General student employment on or off campus */
    STUDENT_EMPLOYMENT,

    /** 6. Faculty-mentored academic inquiry or investigation */
    RESEARCH,

    /** 7. Practical work conducted by a researcher in the natural environment */
    FIELD_WORK,

    /** 8. Competitive programs supporting graduate or post-graduate research */
    FELLOWSHIP,

    /** 9. Academic credit earned through international travel and study */
    STUDY_ABROAD,

    /** 10. Unpaid service to the community or non-profit organizations */
    VOLUNTEERISM,

    /** 11. Projects addressing specific community needs */
    COMMUNITY_PROJECT,

    /** 12. Learning that combines academic objectives with community service */
    SERVICE_LEARNING,

    /** 13. Interactive environments replicating real-world scenarios */
    CLASSROOM_SIMULATION,

    /** 14. Practical work performed in a laboratory setting */
    LAB_WORK,

    /** 15. Creative exhibition or performance in the arts */
    ARTS_PERFORMANCE,

    /** 16. On-the-job training combined with classroom instruction */
    APPRENTICESHIP
}
